// Exemplo 1: O que é OO?
// Orientação a Objetos (OO) é um paradigma de programação baseado em objetos.
// Objetos representam entidades do mundo real com propriedades e comportamentos.
// OO facilita a organização, reutilização e manutenção do código.

// Exemplo de objeto simples:
let pessoa = {
    nome: 'Ana',
    idade: 30,
    falar: function() {
        console.log('Olá!');
    }
};
pessoa.falar();
