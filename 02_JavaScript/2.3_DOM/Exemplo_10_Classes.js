// Exemplo 10: Trabalhando com Classes
// Podemos adicionar, remover e alternar classes CSS

// Suponha <div id="painel"></div>
let painel = document.getElementById('painel');
painel.classList.add('ativo'); // adiciona classe
painel.classList.remove('ativo'); // remove classe
painel.classList.toggle('ativo'); // alterna classe
