// Exemplo 5: Métodos de Arrays (1/4)
// push, pop, shift, unshift

let numeros = [1, 2, 3];
numeros.push(4); // adiciona ao final
console.log('push:', numeros);
numeros.pop(); // remove o último
console.log('pop:', numeros);
numeros.shift(); // remove o primeiro
console.log('shift:', numeros);
numeros.unshift(0); // adiciona no início
console.log('unshift:', numeros);
