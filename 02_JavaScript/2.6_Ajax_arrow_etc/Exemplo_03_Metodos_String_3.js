// Exemplo 3: Métodos de String (3/3)
// replace, split, trim, startsWith, endsWith, includes

let texto = '  JavaScript é incrível!  ';
console.log('replace:', texto.replace('incrível', 'poderoso'));
console.log('split:', texto.split(' '));
console.log('trim:', texto.trim());
console.log('startsWith:', texto.trim().startsWith('Java'));
console.log('endsWith:', texto.trim().endsWith('!'));
console.log('includes:', texto.includes('Script'));
