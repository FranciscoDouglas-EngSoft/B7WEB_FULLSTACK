// Exemplo 2: O que é JSON
// JSON (JavaScript Object Notation) é um formato leve de troca de dados
// Muito usado em APIs para enviar e receber informações

let pessoa = {
    nome: 'Ana',
    idade: 30
};
let json = '{"nome":"Ana","idade":30}';
console.log('Objeto JS:', pessoa);
console.log('JSON:', json);
