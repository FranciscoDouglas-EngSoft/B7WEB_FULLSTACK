// Exemplo 12: String e Template String
// Strings podem ser concatenadas com + ou usando template strings

let nome = 'Lucas';
let saudacao = 'Olá, ' + nome + '!';
console.log(saudacao);

// Template string (crase) permite interpolação de variáveis
let mensagem = `Bem-vindo, ${nome}!`;
console.log(mensagem);
