// Exemplo 17: Desmistificando o IF
// O if pode ser usado para qualquer expressão que resulte em true ou false

let valor = 0;
if (valor) {
    console.log('Valor é verdadeiro');
} else {
    console.log('Valor é falso');
}

// 0, '', null, undefined, NaN são considerados falsos
// Qualquer outro valor é considerado verdadeiro
