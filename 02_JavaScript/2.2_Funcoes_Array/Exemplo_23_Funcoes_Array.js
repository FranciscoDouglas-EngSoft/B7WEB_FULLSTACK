// Exemplo 23: Funções de array
// Arrays possuem métodos úteis como push, pop, shift, unshift, includes, indexOf

let numeros = [1, 2, 3];
numeros.push(4); // adiciona ao final
console.log('Após push:', numeros);
numeros.shift(); // remove o primeiro
console.log('Após shift:', numeros);
console.log('Inclui 2?', numeros.includes(2));
console.log('Índice do 3:', numeros.indexOf(3));
